package com.dat.whm.common.entity;

public enum LeaveStatus {

	PRESENT {
		public String toString() {
			return "Present";
		}
	}
	, FULL_LEAVE {
		public String toString() {
			return "Full leave";
		}
	}
	, HALF_LEAVE {
		public String toString() {
			return "Half leave";
		}
	};

	public abstract String toString();
}