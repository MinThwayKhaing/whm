package com.dat.whm.web.workinghourmanagement.team;


public class YearMonthDivision {
	private int member;
	private double hour;
	private double manMonth;
	
	public YearMonthDivision(int member, double hour, double manMonth) {
		this.member = member;
		this.hour = hour;
		this.manMonth = manMonth;
	}
	
	public int getMember() {
		return member;
	}

	public void addMember(int member) {
		this.member = this.member + member;
	}

	public double getHour() {
		return hour;
	}

	public void addHour(double hour) {
		this.hour = this.hour + hour;
	}

	public double getManMonth() {
		return manMonth;
	}

	public void addManMonth(double manMonth) {
		this.manMonth = this.manMonth + manMonth;
	}
	
}
