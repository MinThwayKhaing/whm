package com.dat.whm.web.workinghourmanagement.individual;

import javax.faces.bean.ManagedBean;
import javax.faces.bean.ViewScoped;


@ManagedBean(name = "WorkingHourManagementIndividualStaffDetailDataBean")
@ViewScoped
public class StaffDetailInfo {
	
	
	private String projectID;
	private String projectName;
	
	private double total_hours = 0.0;
	private double total_mm = 0.0;

	private String year;
	private String month;
	

	public String getProjectID()
	{
		return projectID;
	}
	
	public double getTotal_hours() {
		return total_hours;
	}

	public void setTotal_hours(double total_hours) {
		this.total_hours = total_hours;
	}

	public double getTotal_mm() {
		return total_mm;
	}

	public void setTotal_mm(double total_mm) {
		this.total_mm = total_mm;
	}

	public void setProjectID(String projectID) {
		this.projectID = projectID;
	}

	public void setProjectName(String projectName) {
		this.projectName = projectName;
	}

	public void setYear(String year) {
		this.year = year;
	}

	public void setMonth(String month) {
		this.month = month;
	}

	public String getProjectName()
	{
		return projectName;		
	}
	
	public String getYear()
	{
		return year;
	}

	public String getMonth()
	{
		return month;
	}
	
}
