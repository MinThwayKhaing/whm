package com.dat.whm.web.workinghrmanagementproject;

public class TotalManMonthInfo {
	String projectId;
	double total_hour;
	double total_manmonth;
	String year;
	String month;
	int total_members;

	public double getTotal_hour() {
		return total_hour;
	}

	public void setTotal_hour(double total_hour) {
		this.total_hour = total_hour;
	}

	public double getTotal_manmonth() {
		return total_manmonth;
	}

	public void setTotal_manmonth(double total_manmonth) {
		this.total_manmonth = total_manmonth;
	}

	public String getYear() {
		return year;
	}

	public void setYear(String year) {
		this.year = year;
	}

	public String getMonth() {
		return month;
	}

	public void setMonth(String month) {
		this.month = month;
	}

	public String getProjectId() {
		return projectId;
	}

	public void setProjectId(String projectId) {
		this.projectId = projectId;
	}

	public int getTotal_members() {
		return total_members;
	}

	public void setTotal_members(int total_members) {
		this.total_members = total_members;
	}
	
	

}
