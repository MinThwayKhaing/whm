package com.dat.whm.web.dailyreport;

import java.util.List;

public class RegistrationStatusData {
	private String fullname;
	private String username;
	private String team;
	private List<String> dateList;
	private boolean notCompleteShow;

	public String getFullname() {
		return fullname;
	}

	public void setFullname(String fullname) {
		this.fullname = fullname;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getTeam() {
		return team;
	}

	public void setTeam(String team) {
		this.team = team;
	}

	public List<String> getDateList() {
		return dateList;
	}

	public void setDateList(List<String> dateList) {
		this.dateList = dateList;
	}

	public boolean isNotCompleteShow() {
		return notCompleteShow;
	}

	public void setNotCompleteShow(boolean notCompleteShow) {
		this.notCompleteShow = notCompleteShow;
	}
	
}
